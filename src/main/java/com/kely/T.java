package com.kely;

public class T {
    private int second;
    private String score;

    public T(int second, String score) {
        this.second = second;
        this.score = score;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
